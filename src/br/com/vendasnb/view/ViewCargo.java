    public void carregarCargo(){
        listaModelCargo = controllerCargo.retornaListaCargoController();
        DefaultTableModel modelo = (DefaultTableModel) this.tableCargo.getModel();
        modelo.setNumRows(0);
        // inserir dados na tabela
        for(int i=0; i < listaModelCargo.size(); i++){
            modelo.addRow(new Object[]{
                listaModelCargo.get(i).getIdCargo(),
                listaModelCargo.get(i).getDescricao(),
                listaModelCargo.get(i).getSalario()
            });
        }
    }
    
    public void habilitarDesabilitar(boolean condicao){
        txtCarDesc.setEnabled(condicao);
        txtCarSalario.setEnabled(condicao);
    }
    
    public void limparCampo(){
        txtCarCodigo.setText("");
        txtCarDesc.setText("");
        txtCarSalario.setText("");
    }
    
    /***
     * Salva os fornecedores no banco
     */
    
    
    private void salvarCargo() {
        try {
            modelCargo.setDescricao(this.txtCarDesc.getText());
            modelCargo.setSalario(formatador.converterVirgulaParaPonto(this.txtCarSalario.getText()));

            if (controllerCargo.salvarCargoController(modelCargo) > 0) {
                JOptionPane.showMessageDialog(this, "Cargo cadastrado com sucesso!", "ATENÇÃO", JOptionPane.WARNING_MESSAGE);
                this.carregarCargo();
                this.limparCampo();
                this.habilitarDesabilitar(false);
            }else {
               JOptionPane.showMessageDialog(this, "Erro ao cadastrar o Cargo!", "ERRO", JOptionPane.ERROR_MESSAGE);
            }
        }catch (Exception e) {
           // JOptionPane.showMessageDialog(this, "Erro ao cadastrar o Cargo!", "ERRO", JOptionPane.ERROR_MESSAGE);
           e.printStackTrace();
        }
    }
    
    /***
     * Altera os fornecedores no banco
     */
    private void alterarCargo(){
        try {
            modelCargo.setDescricao(txtCarDesc.getText());
            modelCargo.setSalario(formatador.converterVirgulaParaPonto(txtCarSalario.getText()));
            
            if(controllerCargo.alteraCargoController(modelCargo)){
                JOptionPane.showMessageDialog(this, "Cargo alterado com sucesso!", "ATENÇÃO", JOptionPane.WARNING_MESSAGE);
                this.carregarCargo();
                this.limparCampo();
                habilitarDesabilitar(false);
            }else {
               JOptionPane.showMessageDialog(this, "Erro ao alterar o cargo!", "ERRO", JOptionPane.ERROR_MESSAGE);
            }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, "Erro ao alterar o cargo!", "ERRO", JOptionPane.ERROR_MESSAGE);
        }
    }

