    private void btnCarDeletarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnCarDeletarActionPerformed
        // deletar
        int linha = tableCargo.getSelectedRow();
        int codigoCargo = (int) tableCargo.getValueAt(linha, 0);

        if (controllerCargo.excluirCargoController(codigoCargo)) {
            JOptionPane.showMessageDialog(this, "Fornecedor excluído com sucesso!", "ATENÇÃO", JOptionPane.WARNING_MESSAGE);
            this.carregarCargo();
            this.habilitarDesabilitar(false);
        } else {
            JOptionPane.showMessageDialog(this, "Erro ao excluir o Fornecedor!", "ERRO", JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_btnCarDeletarActionPerformed

