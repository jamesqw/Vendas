    private void btnCarEditarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnCarEditarActionPerformed
        // Editar
        salvarAlterar = "alterar";
         habilitarDesabilitar(true);
        int linha = tableCargo.getSelectedRow();
        try {
            int codigoCargo = (int) tableCargo.getValueAt(linha, 0);
            // recuperar os dados do banco
            modelCargo = controllerCargo.retornaCargoController(codigoCargo);
            // setar na interface
            this.txtCarCodigo.setText(String.valueOf(modelCargo.getIdCargo()));
            this.txtCarDesc.setText(modelCargo.getDescricao());
            this.txtCarSalario.setText(String.valueOf(modelCargo.getSalario()));
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, "Código inválido ou nenhum registro selecionado!", "AVISO", JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_btnCarEditarActionPerformed

